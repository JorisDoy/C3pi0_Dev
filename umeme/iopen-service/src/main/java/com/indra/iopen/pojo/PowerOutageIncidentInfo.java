package com.indra.iopen.pojo;

public class PowerOutageIncidentInfo {
	private String comment = null;
	private String plannedDate = null;
	private String plannedDay = null;
	private String status = null;

	public PowerOutageIncidentInfo() {

	}

	public String getComment() {
		return comment;
	}

	public void setComment(String comment) {
		this.comment = comment;
	}

	public String getPlannedDate() {
		return plannedDate;
	}

	public void setPlannedDate(String plannedDate) {
		this.plannedDate = plannedDate;
	}

	public String getPlannedDay() {
		return plannedDay;
	}

	public void setPlannedDay(String plannedDay) {
		this.plannedDay = plannedDay;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

}
