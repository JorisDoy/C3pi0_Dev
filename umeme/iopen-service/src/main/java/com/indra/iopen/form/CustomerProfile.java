package com.indra.iopen.form;

import java.io.Serializable;

import com.indra.iopen.pojo.ContactPerson;
import com.indra.iopen.pojo.PremiseInformation;

public class CustomerProfile implements Serializable {
	private static final Long serialVersionUID = -2694021069178585983L;
	private String firstName = null;
	private String middleName = null;
	private String surname = null;
	private String idType = null;
	private String idNumber = null;
	private String mobileNumber = null;
	private String officeNumber = null;
	private String emailAddress = null;
	private PremiseInformation premiseInformation = null;
	private ContactPerson contactPerson = null;
	private Long country;

	public CustomerProfile() {

	}

	public String getFirstName() {
		return firstName;
	}

	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}

	public String getMiddleName() {
		return middleName;
	}

	public void setMiddleName(String middleName) {
		this.middleName = middleName;
	}

	public String getSurname() {
		return surname;
	}

	public void setSurname(String surname) {
		this.surname = surname;
	}

	public String getIdType() {
		return idType;
	}

	public void setIdType(String idType) {
		this.idType = idType;
	}

	public String getIdNumber() {
		return idNumber;
	}

	public void setIdNumber(String idNumber) {
		this.idNumber = idNumber;
	}

	public String getMobileNumber() {
		return mobileNumber;
	}

	public void setMobileNumber(String mobileNumber) {
		this.mobileNumber = mobileNumber;
	}

	public String getOfficeNumber() {
		return officeNumber;
	}

	public void setOfficeNumber(String officeNumber) {
		this.officeNumber = officeNumber;
	}

	public String getEmailAddress() {
		return emailAddress;
	}

	public void setEmailAddress(String emailAddress) {
		this.emailAddress = emailAddress;
	}

	public PremiseInformation getPremiseInformation() {
		return premiseInformation;
	}

	public void setPremiseInformation(PremiseInformation premiseInformation) {
		this.premiseInformation = premiseInformation;
	}

	public ContactPerson getContactPerson() {
		return contactPerson;
	}

	public void setContactPerson(ContactPerson contactPerson) {
		this.contactPerson = contactPerson;
	}

	public Long getCountry() {
		return country;
	}

	public void setCountry(Long country) {
		this.country = country;
	}

	@Override
	public String toString() {
		return "CustomerProfile [firstName=" + firstName + ", middleName=" + middleName + ", surname=" + surname
				+ ", idType=" + idType + ", idNumber=" + idNumber + ", mobileNumber=" + mobileNumber + ", officeNumber="
				+ officeNumber + ", emailAddress=" + emailAddress + ", premiseInformation=" + premiseInformation
				+ ", contactPerson=" + contactPerson + ", country=" + country + "]";
	}

}
