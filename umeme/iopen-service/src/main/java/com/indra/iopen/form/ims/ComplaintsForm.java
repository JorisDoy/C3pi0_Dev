package com.indra.iopen.form.ims;

import java.io.Serializable;

import com.indra.iopen.form.CustomerProfile;
import com.indra.iopen.pojo.ComplaintDetail;

public class ComplaintsForm implements Serializable {
	private static final long serialVersionUID = 3031965938092446241L;
	private ComplaintDetail complaintDetail = null;
	private CustomerProfile customerProfile;

	public ComplaintsForm() {

	}

	public ComplaintDetail getComplaintDetail() {
		return complaintDetail;
	}

	public void setComplaintDetail(ComplaintDetail complaintDetail) {
		this.complaintDetail = complaintDetail;
	}

	public CustomerProfile getCustomerProfile() {
		return customerProfile;
	}

	public void setCustomerProfile(CustomerProfile customerProfile) {
		this.customerProfile = customerProfile;
	}

	@Override
	public String toString() {
		return "ComplaintsForm [complaintDetail=" + complaintDetail + ", customerProfile=" + customerProfile + "]";
	}

}
