package com.indra.iopen.form;

import java.io.Serializable;
import java.util.Date;

import javax.validation.constraints.NotNull;

public class ApplicationInvoice implements Serializable {

    private static final long serialVersionUID = 1L;
    @NotNull
    private String invoiceNumber;
    @NotNull
    private String invoiceType;
    @NotNull
    private Date invoiceDate;
    @NotNull
    private Date paymentDate;
    @NotNull
    private double invoiceAmount;
    @NotNull
    private String invoiceStatus;

    @NotNull
    public String getInvoiceNumber() {
        return invoiceNumber;
    }

    public void setInvoiceNumber(final String invoiceNumber) {
        this.invoiceNumber = invoiceNumber;
    }

    public String getInvoiceType() {
        return invoiceType;
    }

    public void setInvoiceType(final String invoiceType) {
        this.invoiceType = invoiceType;
    }

    public Date getInvoiceDate() {
        return invoiceDate;
    }

    public void setInvoiceDate(final Date invoiceDate) {
        this.invoiceDate = invoiceDate;
    }

    public Date getPaymentDate() {
        return paymentDate;
    }

    public void setPaymentDate(final Date paymentDate) {
        this.paymentDate = paymentDate;
    }

    public double getInvoiceAmount() {
        return invoiceAmount;
    }

    public void setInvoiceAmount(final double invoiceAmount) {
        this.invoiceAmount = invoiceAmount;
    }

    public String getInvoiceStatus() {
        return invoiceStatus;
    }

    public void setInvoiceStatus(final String invoiceStatus) {
        this.invoiceStatus = invoiceStatus;
    }

}
